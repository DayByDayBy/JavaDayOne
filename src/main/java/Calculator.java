public class Calculator {

    public int addTo(int first, int second) {
        return first + second;
    }

    public int subtractFrom(int first, int second) {
        return first - second;
    }

    public int multiplyBy(int first, int second) {
        return first * second;
    }

    public double divideBy(double first, double second) {
        return first / second;
    }






}